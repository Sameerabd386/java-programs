// java program to print Solid rectangle pattern

public class rectanglepattern {
    public static void main(String[] args) {
     int m = 4; // for rows
     int n = 5; // for columns
     for(int i=1; i<=m; i++) 
     {
      for(int j=1; j<=n; j++) 
       {
        System.out.print("*");
       }
        System.out.println();
     }
    }
  }

